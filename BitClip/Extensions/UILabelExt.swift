import UIKit

extension UILabel {
    func setNegativeColor() {
        backgroundColor = #colorLiteral(red: 0.5807225108, green: 0.066734083, blue: 0, alpha: 1)
    }
    
    func setPositiveColor() {
        backgroundColor = #colorLiteral(red: 0.3084011078, green: 0.5618229508, blue: 0, alpha: 1)
    }
}
